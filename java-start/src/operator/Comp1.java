package operator;

public class Comp1 {
    public static void main(String[] args) {

        int a = 2;
        int b = 3;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        //비교연산자는 결과를 boolean변수에 담을수 있다.

        boolean result = a == b;
        System.out.println(result);




    }
}
