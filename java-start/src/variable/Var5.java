package variable;

public class Var5 {
    public static void main(String[] args) {
        //1.변수선언
        int a;
        a = 1;//변수 초기화
        System.out.println(a);

        //선언과 초기화를 한번에
        int b = 2;
        System.out.println(b);

        //여러변수 선언과 함께 초기화를 한번에
        int c =3, d=4;
        System.out.println(c);
        System.out.println(d);
    }
}
